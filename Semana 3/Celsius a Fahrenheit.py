# Desarrolle una función llamada celsius2fahrenheit que tome como parámetro un número,
# que representa una temperatura en grados Celsius, y devuelva su equivalente en grados Fahrenheit.

# Nota: La conversión de grados Celsius (C) a Fahrenheit (F) se hace según al fórmula F = (C * 9/5) + 32

# Contenido del módulo main.py

def celsius2fahrenheit(celsius_num):

    return (celsius_num * 9 / 5) + 32