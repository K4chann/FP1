# Escriba un programa que lea del teclado dos números enteros que representan la base y la altura de un rectángulo y muestre en pantalla su perímetro.

# Ejemplo:

# Base: 3
# Altura: 5
# Perímetro: 16

base = int(input("Base: "))
altura = int(input("Altura: "))
print("Perímetro:", (base * 2) + (altura * 2))
